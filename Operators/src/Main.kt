fun main() {

    var x = 5
    val y = 3

    var result = x + y //not neccesary

    result = result + 2// result += 2

    println("my result is $result")

    println("result ${x + y}") //place holder

    println("result ${x * y}")

    println("result ${x / y}")

    println("result ${x % y}")

    println("result ${x++}")// increment +1

    println("result ${x--}") //decrement -1
    println("result ${x - y}")
    //operator preccedence bodmas is followed
}